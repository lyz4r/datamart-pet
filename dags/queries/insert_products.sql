-- Проверим результат
SELECT COUNT(*) as total_products FROM eshop.products;

-- Посмотрим распределение по категориям
SELECT 
    category, 
    COUNT(*) as count,
    MIN(price) as min_price,
    MAX(price) as max_price,
    ROUND(AVG(price)) as avg_price
FROM eshop.products
GROUP BY category
ORDER BY count DESC;

